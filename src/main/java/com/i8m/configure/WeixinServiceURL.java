package com.i8m.configure;

public class WeixinServiceURL {
	
	private String accessToken;
	
	private String message;
	
	private String mediaUpload;
	
	private String medaiList;

	public String getAccessToken() {
		return accessToken;
	}

	public void setAccessToken(String accessToken) {
		this.accessToken = accessToken;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getMediaUpload() {
		return mediaUpload;
	}

	public void setMediaUpload(String mediaUpload) {
		this.mediaUpload = mediaUpload;
	}

	public String getMedaiList() {
		return medaiList;
	}

	public void setMedaiList(String medaiList) {
		this.medaiList = medaiList;
	}
	
	

}
